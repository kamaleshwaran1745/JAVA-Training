class bitopchk{
    public static void main(String args[]){
        int a=60;
        int b=13;
        int c;
        int d;
        int f;
        int e;
        c=a&b;
        d=a|b;
        f=a^b;
        e=~a;
        System.out.println("AND :"+c);
        System.out.println("OR :"+d);
        System.out.println("Ex-OR :"+f);
        System.out.println("NOT :"+e);
    }
}